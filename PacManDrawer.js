var PacManDrawer = function()
{
	this.bounds = {};
	this.bounds.r = pacman_imgdata[0][0].length;
	this.bounds.c = pacman_imgdata[0][0][0].length;
	
	this.draw = function(ctx,gfxScale,pixelPos,direction,walkState,color,centerGfxFlag)
	{
		ctx.fillStyle = color;
		
		var anchoredPos = {};
		anchoredPos.x = pixelPos.x;
		anchoredPos.y = pixelPos.y;
		
		if(centerGfxFlag)
		{
			anchoredPos.x = pixelPos.x-7*gfxScale;
			anchoredPos.y = pixelPos.y-8*gfxScale;
		}
		
		ctx.save();
		ctx.translate(Math.floor(anchoredPos.x),Math.floor(anchoredPos.y));
	
		var imgdata = pacman_imgdata[direction][walkState];
		
		Drawers.drawData(ctx,imgdata,gfxScale);
				
		ctx.restore();
	}
	
	this.drawInRectangle = function(ctx,rect,direction,walkState,color)
	{
		var gfxScale = 0;
		if(rect.width < rect.height)
		{
			gfxScale = rect.width/this.bounds.c;
		}
		else
		{
			gfxScale = rect.height/this.bounds.r;
		}
		
		this.draw(ctx,gfxScale,rect.position,direction,walkState,color,false);
	}
	
		
	
}
